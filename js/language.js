window.currentLang = localStorage.getItem('lang') || 'en'; // Default to English
let _currentPlantationCount = 0; // Global variable to store plantation count

function setLanguage(lang) {
    console.log(`Setting language to: ${lang}`);
    window.currentLang = lang;
    localStorage.setItem('lang', lang);

    if (window.location.pathname.endsWith('maps.html')) {
        location.reload();
        return; 
    }

    applyTranslations();
    updateLanguageSwitcher();
    updatePlantationCount(); // Call update with stored count
    if (typeof updateMapTranslations === 'function') {
        updateMapTranslations();
    }
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');

        // Handle placeholder translation
        if (key.startsWith('[placeholder]')) {
            const placeholderKey = key.substring('[placeholder]'.length);
            if (window.translations[window.currentLang] && window.translations[window.currentLang][placeholderKey]) {
                element.placeholder = window.translations[window.currentLang][placeholderKey];
            }
            return; // Move to the next element
        }

        // Skip plantation-count as it's handled by updatePlantationCount
        if (element.id === 'plantation-count') {
            return;
        }

        if (window.translations[window.currentLang] && window.translations[window.currentLang][key]) {
            element.textContent = window.translations[window.currentLang][key];
        }
    });

    // Update title separately
    const titleElement = document.querySelector('title');
    if (titleElement && window.translations[window.currentLang] && window.translations[window.currentLang][titleElement.getAttribute('data-i18n')]) {
        titleElement.textContent = window.translations[window.currentLang][titleElement.getAttribute('data-i18n')];
    }
}

function updateLanguageSwitcher() {
    const switchers = document.querySelectorAll('.language-switcher, .language-switcher-mobile');
    switchers.forEach(switcher => {
        if (!switcher) return;

        // Handle standard <select> dropdown (desktop)
        if (switcher.tagName === 'SELECT') {
            switcher.value = window.currentLang;
        }

        // Handle custom <div> dropdown (mobile)
        if (switcher.classList.contains('custom-dropdown')) {
            const selectedLangSpan = switcher.querySelector('.selected-lang');
            if (selectedLangSpan) {
                selectedLangSpan.textContent = window.currentLang.toUpperCase();
            }
        }
    });
}

// New function to update plantation count with translation
function updatePlantationCount(count = _currentPlantationCount) {
    _currentPlantationCount = count; // Store the count
    const plantationCountElement = document.getElementById('plantation-count');
    if (plantationCountElement && window.translations[window.currentLang] && window.translations[window.currentLang].plantationCount) {
        plantationCountElement.textContent = window.translations[window.currentLang].plantationCount.replace('{count}', count);
    }
}

document.addEventListener('DOMContentLoaded', () => {
    console.log('Language script loaded');
    applyTranslations();
    updateLanguageSwitcher();

    const switchers = document.querySelectorAll('.language-switcher, .language-switcher-mobile');
    switchers.forEach(switcher => {
        if (switcher) {
            switcher.addEventListener('change', (event) => {
                setLanguage(event.target.value);
            });
        }
    });
});